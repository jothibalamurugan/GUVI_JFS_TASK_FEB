package com.JSFTask1;

public class T1PrintTheNumber {
    public static void main(String[] args)
    {
        System.out.println("----For loop-----");
        for (int i = 10; i <=50 ; i++)
        {
            System.out.println(i);
        }

        System.out.println("----while loop-----");
        int j=10;
        while(j<=50)
        {
            System.out.println(j);
            j++;
        }
    }
}
